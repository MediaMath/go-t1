import (
	"bytes"
	"net/url"
	"sync"
)

var (
	bufferPool *sync.Pool
	valuesPool *sync.Pool
)

func init() {
	bufferPool = &sync.Pool{
		New: func() interface{} {
			return new(bytes.Buffer)
		},
	}
	valuesPool = &sync.Pool{
		New: func() interface{} {
			return make(url.Values)
		},
	}
}

func putBufferBackInPool(b *bytes.Buffer) {
	b.Reset()
	bufferPool.Put(b)
}

func putValuesBackInPool(v url.Values) {
	for key := range v {
		v.Del(key)
	}
	valuesPool.Put(v)
}
